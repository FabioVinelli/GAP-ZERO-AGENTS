function hermesMcpConfigYaml(name, agentDir, toolNames) {
  const serverName = `${name}-mcp`;
  const pythonPath = absAgentPath(agentDir, ".venv/bin/python");
  const serverPath = absAgentPath(agentDir, "mcp_server/server.py");
  const include = toolNames.length ? `[${toolNames.join(", ")}]` : "[]";
  return `# Paste under mcp_servers: in ~/.hermes/config.yaml
# EDIT absolute paths after git transfer / clone on another machine
mcp_servers:
  ${serverName}:
    command: "${pythonPath}"  # EDIT after transfer
    args: ["${serverPath}"]  # EDIT after transfer
    env: {}
    timeout: 120
    enabled: true
    tools:
      include: ${include}
      resources: false
      prompts: false
`;
}


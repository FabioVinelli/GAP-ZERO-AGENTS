function wiringToolsPy(wiring) {
  if (!wiring.length) {
    wiring = [{ step: "example_action", mechanism: "API/REST", landingCheck: "response id returned", reversibility: "reversible" }];
  }
  return wiring
    .map((w, i) => {
      const fn = toolSlug(w.step) || `tool_${i}`;
      const irreversible = w.reversibility === "irreversible";
      const needsApproval = !irreversible && w.requiresApproval === true;
      const riskClass = irreversible ? "irreversible" : needsApproval ? APPROVAL_RISK_CLASS : "low";
      const permission = irreversible || needsApproval ? "write-with-validation" : "write";
      return `    def ${fn}_execute(args):
        # TODO wire real mechanism: ${w.mechanism}
        return {"status": "TODO", "echo": args}

    def ${fn}_landing(args, out):
        # TODO deterministic check: ${w.landingCheck || "verify the action landed"}
        return out.get("status") not in (None, "TODO-FAILED")

    registry.register(ToolSpec(
        name=${JSON.stringify(fn)},
        description=${JSON.stringify(`${w.step} via ${w.mechanism}. Landing check: ${w.landingCheck || "TBD"}`)},
        mechanism=${JSON.stringify(w.mechanism || "GAP")},
        reversibility=${JSON.stringify(w.reversibility || "reversible")},
        permission=${JSON.stringify(permission)},
        input_schema={"type": "object", "properties": {"payload": {"type": "string"}}, "required": []},
        execute=${fn}_execute,
        landing_check=${fn}_landing,
        risk_class=${JSON.stringify(riskClass)},
    ))`;
    })
    .join("\n\n");
}


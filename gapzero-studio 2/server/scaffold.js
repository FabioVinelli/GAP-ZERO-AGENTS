function policyRulesPy(rules) {
  const entries = rules.length
    ? rules.map((r) => `    ${JSON.stringify(r.riskClass)}: ${JSON.stringify(r.decision)},`).join("\n")
    : `    "financial": "escalate",\n    "irreversible": "escalate",\n    "low": "allow",`;
  return `RULES = {\n${entries}\n}`;
}


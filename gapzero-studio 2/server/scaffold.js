/** Shared slug for wiring steps — used by Python stubs and (later) MCP configs. */
export function toolSlug(step) {
  return slug(step || "step").replace(/-/g, "_");
}

const APPROVAL_RISK_CLASS = "requires-validator-approval";

/** Merge blueprint policyRules with auto-generated escalate rule for requiresApproval wiring. */
function mergedPolicyRules(bp) {
  const rules = [...(bp.policyRules || [])];
  const needsApprovalRule = (bp.wiring || []).some(
    (w) => w.requiresApproval && w.reversibility !== "irreversible"
  );
  const hasRule = rules.some((r) => r.riskClass === APPROVAL_RISK_CLASS);
  if (needsApprovalRule && !hasRule) {
    rules.push({ riskClass: APPROVAL_RISK_CLASS, decision: "escalate" });
  }
  return rules;
}

export async function generateScaffold(bp, outRoot, options = {}) {
  const name = slug(bp.agentName);
  const agentDir = options.agentDir || path.join(outRoot, name);
  const mcp = resolveMcpEnabled(bp, options.mcpEnabled ?? "auto");
  const files = buildFiles(bp, name, { mcpEnabled: mcp.enabled, mcpReasons: mcp.reasons, agentDir });
  await fs.mkdir(agentDir, { recursive: true });
  for (const [rel, content] of Object.entries(files)) {
    const p = path.join(agentDir, rel);
    await fs.mkdir(path.dirname(p), { recursive: true });
    await fs.writeFile(p, content, "utf8");
  }
  return { dir: agentDir, files: Object.keys(files), mcp: mcp.enabled, mcpReasons: mcp.reasons };
}

function resolveMcpEnabled(bp, userOverride = "auto") {
  return deriveMcpDecision(bp, userOverride);
}


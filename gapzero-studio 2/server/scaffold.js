/** Ordered tool slugs for all wiring steps — matches src/tools.py registry names. */
export function mcpToolNames(wiring) {
  const steps = wiring?.length
    ? wiring
    : [{ step: "example_action", mechanism: "API/REST", landingCheck: "response id returned", reversibility: "reversible" }];
  return steps.map((w, i) => toolSlug(w.step) || `tool_${i}`);
}

/** Hermes tools.include default — excludes irreversible and requiresApproval wiring. */
function mcpHermesIncludeTools(wiring) {
  const steps = wiring?.length ? wiring : [];
  return steps
    .filter((w) => w.reversibility !== "irreversible" && w.requiresApproval !== true)
    .map((w, i) => toolSlug(w.step) || `tool_${i}`);
}

function absAgentPath(agentDir, ...parts) {
  return path.resolve(agentDir, ...parts);
}

function mcpServerPy(mcpName) {
  return `"""MCP exposure layer — wraps src/tools.py ToolSpecs. Do not duplicate tool logic."""
import os
import sys

# Agent root on sys.path regardless of platform CWD
sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from dotenv import load_dotenv
load_dotenv()  # standalone process; platform env overrides still win

from mcp.server.fastmcp import FastMCP
from src.gapzero.registry import ActuationRegistry
from src.tools import register_tools

mcp = FastMCP(${JSON.stringify(mcpName)})

def _build_registry() -> ActuationRegistry:
    reg = ActuationRegistry()
    register_tools(reg)
    return reg

def _register_mcp_tools():
    reg = _build_registry()
    for spec in reg.tools.values():
        def make_handler(s):
            async def handler(**kwargs):
                out = s.execute(kwargs)
                if not s.landing_check(kwargs, out):
                    raise ValueError(f"landing-check-failed: {s.name}")
                return out
            handler.__name__ = s.name
            handler.__doc__ = s.description
            return handler
        mcp.tool()(make_handler(spec))  # closure-safe binding

_register_mcp_tools()

if __name__ == "__main__":
    mcp.run()  # stdio default
`;
}

